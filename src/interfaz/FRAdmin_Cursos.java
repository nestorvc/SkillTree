    private void btnCrearModificarModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearModificarModificarActionPerformed
        String codigo = txtCodigo.getText().trim();
        String nombre = txtNombre.getText().trim();
        int horas = (Integer) spnHoras.getValue();
        int creditos = (Integer) spnCreditos.getValue();
        String contenido = txtContenido.getText();
        boolean enUso = chkEstado.isSelected();

        //TAB VISTA ACTIVADA
        if (vistaActivado) {
            int cualFila = tbCursos.getSelectedRow();
            Curso c = getUi().getRegCursos().getListaCursos().get(cualFila);
            buscarCurso(c.getCodigo(), c.getNombre());
            return;
        }

        if (!camposVacios()) {
            //TAB CREAR ACTIVADA
            if (crearActivado) {
                if (getUi().crearCurso(codigo, nombre, horas, creditos, contenido, iconoSeleccionado)) {
                    lblMensaje.setText("Curso creado.");
                    lblMensaje.setForeground(Color.black);
                    limpiar();
                } else {
                    lblMensaje.setText("El curso ya existe. Escriba un cÃ³digo y/o nombre diferentes.");
                    lblMensaje.setForeground(Color.red);
                }
            }
            //TAB MODIFICAR ACTIVADA
            if (modificarActivado) {
                getUi().modificarCurso(codigo, nombre, horas, creditos, contenido, iconoSeleccionado, enUso);
                lblMensaje.setText("Curso modificado.");
                lblMensaje.setForeground(Color.black);
            }
        } else {
            lblMensaje.setText("Por favor complete todos los campos.");
            lblMensaje.setForeground(Color.red);
        }
    }//GEN-LAST:event_btnCrearModificarModificarActionPerformed

