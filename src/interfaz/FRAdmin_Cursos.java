    public boolean camposVacios() {
        if (txtCodigo.getText().equals("")
                || txtNombre.getText().equals("")
                || spnHoras.getValue().equals(0)
                || spnCreditos.getValue().equals(0)
                || txtContenido.getText().equals("")
                || iconoSeleccionado == null) {
            return true;
        } else {
            return false;
        }
    }

